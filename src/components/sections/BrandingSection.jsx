export default function BrandingSection() {
  const track1Ref = useRef(null);
  const track2Ref = useRef(null);
  const tlRef = useRef(null);

  useEffect(() => {
    // Dynamically import GSAP to work with Next.js SSR
    const initGSAP = async () => {
      const { gsap } = await import("gsap");

      const duration = 28;

      // Track 1: moves from 0 → -50%
      tlRef.current = gsap.timeline({ repeat: -1, defaults: { ease: "none" } });
      gsap.set(track1Ref.current, { x: "0%" });
      gsap.set(track2Ref.current, { x: "0%" });

      gsap.to([track1Ref.current, track2Ref.current], {
        x: "-50%",
        duration,
        ease: "none",
        repeat: -1,
      });
    };

    initGSAP();

    return () => {
      if (tlRef.current) tlRef.current.kill();
    };
  }, []);

  const doubledBrands = [...brands, ...brands];

  return (
    <div className="relative w-full h-[50vh] bg-[#202020] flex flex-col items-center justify-center overflow-hidden">
     
      {/* ── Marquee wrapper ── */}
      <div className="relative w-full overflow-hidden">
        {/* Left fade */}
        <div className="absolute left-0 top-0 h-full w-32 z-10 pointer-events-none bg-gradient-to-r from-[#202020] to-transparent" />
        {/* Right fade */}
        <div className="absolute right-0 top-0 h-full w-32 z-10 pointer-events-none bg-gradient-to-l from-[#202020] to-transparent" />

        {/* Single GSAP-controlled track — doubled for seamless loop */}
        <div
          ref={track1Ref}
          className="flex items-center gap-0 will-change-transform"
          style={{ width: "max-content" }}
        >
          {doubledBrands.map((brand, i) => (
            <BrandCard key={`${brand.name}-${i}`} brand={brand} index={i} />
          ))}
        </div>
      </div>

    </div>
  );
}

